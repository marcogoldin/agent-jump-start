test("absorb writes only the layered leaf spec file", () => {
  const tempDir = makeTempDir();
  try {
    const basePath = writeSpec(tempDir, makeMinimalSpec(), "base.yaml");
    const leafPath = join(tempDir, "leaf.yaml");
    writeFileSync(leafPath, `${JSON.stringify({
      extends: "./base.yaml",
      workspaceInstructions: {
        sections: [{ title: "Leaf rules", rules: ["Leaf only rule."] }],
      },
    }, null, 2)}\n`, "utf8");

    const baseBefore = readFileSync(basePath, "utf8");
    writeFileSync(join(tempDir, "CLAUDE.md"), "## Validation\n\n```bash\nnpm run lint\n```\n", "utf8");

    const selectionPath = join(tempDir, "selection.json");
    writeFileSync(selectionPath, `${JSON.stringify({
      version: 1,
      decisions: [{ path: "CLAUDE.md", choice: "primary" }],
    }, null, 2)}\n`, "utf8");

    expectSuccess(runCli(["absorb", "--spec", leafPath, "--target", tempDir, "--apply", "--selection", selectionPath]));
    assert.equal(readFileSync(basePath, "utf8"), baseBefore, "base layer must remain unchanged");
    assert.match(readFileSync(leafPath, "utf8"), /"extends": "\.\/base\.yaml"/, "leaf must preserve extends");
    assert.match(readFileSync(leafPath, "utf8"), /npm run lint/);
  } finally {
    cleanupTempDir(tempDir);
  }
});

test("init and sync refusal output contains absorb bridge tip", () => {
  const tempDir = makeTempDir();
  try {
    writeFileSync(join(tempDir, "CLAUDE.md"), "SENTINEL\n", "utf8");
    const initResult = runCli(["init", "--target", tempDir, "--non-interactive"]);
    expectFailure(initResult);
    assert.match(initResult.stdout + initResult.stderr, /agent-jump-start absorb/);

    const specPath = writeSpec(tempDir, makeMinimalSpec(), "spec.yaml");
    const syncResult = runCli(["sync", "--spec", specPath, "--target", tempDir]);
    expectFailure(syncResult);
    assert.match(syncResult.stdout + syncResult.stderr, /agent-jump-start absorb/);
  } finally {
    cleanupTempDir(tempDir);
  }
});

test("absorb dry-run is idempotent after sync --force manages all targets", () => {
  const tempDir = makeTempDir();
  try {
    const specPath = writeSpec(tempDir, makeMinimalSpec(), "spec.yaml");
    writeFileSync(join(tempDir, "CLAUDE.md"), "## Validation\n\n```bash\nnpm run lint\n```\n", "utf8");

    const selectionPath = join(tempDir, "selection.json");
    writeFileSync(selectionPath, `${JSON.stringify({
      version: 1,
      decisions: [{ path: "CLAUDE.md", choice: "primary" }],
    }, null, 2)}\n`, "utf8");
    expectSuccess(runCli(["absorb", "--spec", specPath, "--target", tempDir, "--apply", "--selection", selectionPath]));
    expectSuccess(runCli(["sync", "--spec", specPath, "--target", tempDir, "--force"]));

    const result = runCli(["absorb", "--spec", specPath, "--target", tempDir, "--dry-run"]);
    expectSuccess(result);
    assert.match(result.stdout + result.stderr, /Nothing to absorb/);
  } finally {
    cleanupTempDir(tempDir);
  }
});
