function renderReviewChecklist(spec, specRelativePath) {
  const references = [
    ".agents/AGENTS.md",
    "AGENTS.md",
    "AGENT.md",
    "CLAUDE.md",
    "GEMINI.md",
    ".github/copilot-instructions.md",
    ".github/instructions/general.instructions.md",
    ".cursor/rules/agent-instructions.mdc",
    ".windsurf/rules/general.md",
    ".windsurfrules",
    ".clinerules/general.md",
    ".roo/rules/agent-instructions.md",
    ".roorules",
    ".continue/rules/agent-instructions.md",
    ".amazonq/rules/general.md",
    ".junie/AGENTS.md",
    ".junie/guidelines.md",
    "CONVENTIONS.md",
    ...(spec.skills ?? []).flatMap((skill) => [
      `.agents/skills/${skill.slug}/SKILL.md`,
      `.claude/skills/${skill.slug}/SKILL.md`,
      `.github/skills/${skill.slug}/SKILL.md`,
      `.agents/skills/${skill.slug}/AGENTS.md`,
      `.claude/skills/${skill.slug}/AGENTS.md`,
      `.cursor/rules/${skill.slug}.mdc`,
    ]),
  ];
  const checklist = spec.reviewChecklist;
  const lines = [
    "# Review Checklist For Agents",
    "",
    generatedNotice(specRelativePath).trimEnd(),
    "",
    checklist.intro,
    "",
    "## Usage",
    "",
    `Treat a response or proposal as misaligned if it fails ${checklist.failureThreshold} or more checks without a deliberate reason.`,
    "",
    "## Checklist",
    "",
  ];

  checklist.items.forEach((item, index) => {
    lines.push(`${index + 1}. ${item.title}`, "");
    for (const detail of item.details ?? []) {
      lines.push(`   ${detail}`, "");
    }
  });

  if (checklist.quickSignals?.length) {
    lines.push("## Quick Signals", "");
    lines.push(formatBulletList(checklist.quickSignals), "");
  }

  if (checklist.redFlags?.length) {
    lines.push("## Red Flags", "");
    lines.push(formatBulletList(checklist.redFlags), "");
  }

  lines.push("## References", "");
  lines.push(formatBulletList(references), "");

  return `${lines.join("\n").trimEnd()}\n`;
}

// ---------------------------------------------------------------------------
// Main orchestrator: generate all files from a spec
// ---------------------------------------------------------------------------

export function renderGeneratedFiles(spec, specPath, targetRoot) {
  const specAbsolutePath = resolve(specPath);
  const targetAbsolutePath = resolve(targetRoot);
  const specRelativePath = relative(targetAbsolutePath, specAbsolutePath) || specPath;
  const generatedFiles = {};

  // --- Canonical workspace governance file ---
  const canonicalPath = ".agents/AGENTS.md";
  const canonicalDoc = renderWorkspaceInstructions(spec, specRelativePath);
  generatedFiles[canonicalPath] = canonicalDoc;

  // --- Canonical mirrors (workspace-level instruction file variants) ---
  const nativeMirrorDoc = renderWorkspaceInstructions(spec, specRelativePath, { mirrorOf: canonicalPath });
  generatedFiles["AGENTS.md"] = nativeMirrorDoc;
  generatedFiles["AGENT.md"] = nativeMirrorDoc;
  generatedFiles["CLAUDE.md"] = nativeMirrorDoc;
  generatedFiles[".github/copilot-instructions.md"] = nativeMirrorDoc;
  generatedFiles[".github/instructions/general.instructions.md"] = `---\napplyTo: "**"\n---\n\n${nativeMirrorDoc}`;

  // --- Cursor (MDC format with frontmatter) ---
  generatedFiles[".cursor/rules/agent-instructions.mdc"] = renderCursorMdc(spec, specRelativePath);

  // --- Agents WITHOUT native skill folders (get workspace doc + inline skill summaries) ---
  const workspaceDocWithSkills = renderWorkspaceInstructions(spec, specRelativePath, { includeSkillSummaries: true, mirrorOf: canonicalPath });
  generatedFiles["GEMINI.md"] = workspaceDocWithSkills;
  generatedFiles[".amazonq/rules/general.md"] = workspaceDocWithSkills;
  generatedFiles[".junie/AGENTS.md"] = workspaceDocWithSkills;
  generatedFiles[".junie/guidelines.md"] = workspaceDocWithSkills;
  generatedFiles[".windsurf/rules/general.md"] = `---\ntrigger: always_on\n---\n\n${workspaceDocWithSkills}`;
  generatedFiles[".windsurfrules"] = workspaceDocWithSkills;
  generatedFiles[".clinerules/general.md"] = workspaceDocWithSkills;
  generatedFiles[".roo/rules/agent-instructions.md"] = workspaceDocWithSkills;
  generatedFiles[".roorules"] = workspaceDocWithSkills;
  generatedFiles[".continue/rules/agent-instructions.md"] = workspaceDocWithSkills;
  generatedFiles["CONVENTIONS.md"] = workspaceDocWithSkills;

  // --- Skills ---
  for (const skill of spec.skills ?? []) {
    const agentsSkillDir = `.agents/skills/${skill.slug}`;
    const claudeSkillDir = `.claude/skills/${skill.slug}`;
    const githubSkillDir = `.github/skills/${skill.slug}`;
    const canonicalSkillPackage = buildCanonicalSkillPackage(skill, specRelativePath);

    // Canonical, interoperable skill package
    mountSkillPackage(generatedFiles, agentsSkillDir, canonicalSkillPackage, { includeLegacyGuide: true });

    // Native mirrors projected from the canonical package
    mountSkillPackage(generatedFiles, claudeSkillDir, canonicalSkillPackage, { includeLegacyGuide: true });
    mountSkillPackage(generatedFiles, githubSkillDir, canonicalSkillPackage);

    // Cursor skill files (MDC format)
    generatedFiles[`.cursor/rules/${skill.slug}.mdc`] = renderCursorSkillMdc(skill, specRelativePath);
  }

  // --- Review Checklist ---
  if (spec.reviewChecklist) {
    generatedFiles["docs/agent-review-checklist.md"] = renderReviewChecklist(spec, specRelativePath);
  }

  // --- Manifest ---
  // The manifest must include itself in its `files` list so that every
  // rendered artifact is accounted for in a single source of truth.
  // This keeps cleanStaleFiles and checkGeneratedFiles symmetric with
  // the actual on-disk footprint and avoids a class of "needs a second
  // sync" issues where a stale path persists because no manifest layer
  // tracked it.
  const manifestPath = "docs/agent-jump-start/generated-manifest.json";
  const allPaths = [...Object.keys(generatedFiles), manifestPath].sort();
  generatedFiles[manifestPath] = `${JSON.stringify(
    {
      generatedBy: `Agent Jump Start v${TOOL_VERSION}`,
      generatedFrom: specRelativePath,
      generatorVersion: TOOL_VERSION,
      agents: [
        "Claude Code", "GitHub Copilot", "Gemini CLI", "Amazon Q Developer",
        "JetBrains Junie", "GitHub Agents", "Cursor", "Windsurf", "Cline",
        "Roo Code", "Continue.dev", "Aider",
      ],
      files: allPaths,
    },
    null,
    2,
  )}\n`;

  return generatedFiles;
}
