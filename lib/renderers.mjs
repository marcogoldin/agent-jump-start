function renderCategoryTable(categories) {
  const lines = [
    "| Priority | Category | Impact | Prefix |",
    "|----------|----------|--------|--------|",
  ];
  for (const category of sortedCategories(categories)) {
    lines.push(
      `| ${category.priority} | ${category.name} | ${category.impact} | \`${category.prefix}\` |`,
    );
  }
  return lines.join("\n");
}

function renderQuickReference(skill) {
  const lines = [];
  for (const category of groupRulesByCategory(skill)) {
    lines.push(`### ${category.priority}. ${category.name} (${category.impact})`, "");
    for (const rule of category.rules) {
      lines.push(`- \`${rule.id}\` - ${rule.summary}`);
    }
    lines.push("");
  }
  return lines.join("\n").trimEnd();
}

function renderDetailedGuidance(skill) {
  const lines = ["## Detailed Guidance", ""];
  for (const category of groupRulesByCategory(skill)) {
    lines.push(`### ${category.name}`, "");
    for (const rule of category.rules) {
      const semanticTag = rule.semantic === "prohibition" ? " [PROHIBITION]" :
                          rule.semantic === "workflow" ? " [WORKFLOW]" :
                          rule.semantic === "example" ? " [EXAMPLE]" : "";
      lines.push(`#### ${rule.id} - ${rule.title}${semanticTag}`, "");
      lines.push(`**Impact:** ${rule.impact}`, "");
      if (rule.semantic === "prohibition") {
        lines.push(`> **MUST NOT:** ${rule.summary}`, "");
      } else {
        lines.push(rule.summary, "");
      }
      if (rule.guidance?.length) {
        lines.push(formatBulletList(rule.guidance), "");
      }
    }
  }
  return lines.join("\n").trimEnd();
}

function renderSkillSummarySection(skill) {
  const lines = [
    `### ${skill.title}`,
    "",
    `**Version ${skill.version}**${skill.author ? ` — ${skill.author}` : ""}`,
    "",
    skill.description,
    "",
    "**When to apply:** " + skill.appliesWhen.join("; "),
    "",
    renderQuickReference(skill),
    "",
  ];
  return lines.join("\n").trimEnd();
}

// ---------------------------------------------------------------------------
// Reference / scripts / assets table renderers
// ---------------------------------------------------------------------------

function renderReferenceTable(skill, referenceBasePath = "references") {
  if (!skill.references?.length) return "";

  const lines = [
    "## Reference Guide",
    "",
    "Load detailed guidance based on context:",
    "",
    "| Topic | Reference | Load When |",
    "|-------|-----------|-----------|",
  ];

  for (const ref of skill.references) {
    const topic = ref.name.replace(/\.md$/, "").replace(/[-_]/g, " ");
    const loadWhen = ref.loadWhen ?? topic;
    lines.push(`| ${topic} | \`${referenceBasePath}/${ref.name}\` | ${loadWhen} |`);
  }

  lines.push("");
  return lines.join("\n");
}

function renderScriptsTable(skill, scriptsBasePath = "scripts") {
  if (!skill.scripts?.length) return "";

  const lines = [
    "## Bundled Scripts",
    "",
    "Executable scripts available for this skill:",
    "",
    "| Script | Path | Description |",
    "|--------|------|-------------|",
  ];

  for (const script of skill.scripts) {
    const label = script.name.replace(/[-_]/g, " ").replace(/\.[^.]+$/, "");
    const description = script.description ?? label;
    lines.push(`| ${label} | \`${scriptsBasePath}/${script.name}\` | ${description} |`);
  }

  lines.push("");
  return lines.join("\n");
}

function renderAssetsTable(skill, assetsBasePath = "assets") {
  if (!skill.assets?.length) return "";

  const lines = [
    "## Assets",
    "",
    "Static resources bundled with this skill:",
    "",
    "| Asset | Path | Description |",
    "|-------|------|-------------|",
  ];

  for (const asset of skill.assets) {
    const label = asset.name.replace(/[-_]/g, " ").replace(/\.[^.]+$/, "");
    const description = asset.description ?? label;
    lines.push(`| ${label} | \`${assetsBasePath}/${asset.name}\` | ${description} |`);
  }

  lines.push("");
  return lines.join("\n");
}

