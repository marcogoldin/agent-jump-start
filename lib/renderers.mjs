function generatedNotice(specRelativePath) {
  return `> Generated by Agent Jump Start v${TOOL_VERSION} from \`${specRelativePath}\`.\n> Update the canonical spec and rerun \`render\` instead of hand-editing this file.\n`;
}

export function buildSkillDiscoveryDescription(skill, maxLength = 200) {
  const base = skill.description.trim();
  const usageHint = skill.appliesWhen
    .map((entry) => entry.trim())
    .filter(Boolean)
    .slice(0, 3)
    .join("; ");

  if (!usageHint) {
    return truncateAtWordBoundary(base, maxLength);
  }

  return truncateAtWordBoundary(`${base} Use when: ${usageHint}.`, maxLength);
}

function sortedCategories(categories) {
  return [...categories].sort((left, right) =>
    left.priority - right.priority || left.name.localeCompare(right.name),
  );
}

function groupRulesByCategory(skill) {
  return sortedCategories(skill.categories).map((category) => ({
    ...category,
    rules: skill.rules.filter((rule) => rule.category === category.name),
  }));
}

