function renderSkillBody(
  skill,
  specRelativePath,
  { includeCompatibilityNotice = false, referenceBasePath = null, scriptsBasePath = null, assetsBasePath = null } = {},
) {
  const lines = [`# ${skill.title}`, ""];

  if (skill.author) {
    lines.push(`**Version ${skill.version}**  `, skill.author, "");
  } else {
    lines.push(`**Version ${skill.version}**`, "");
  }

  if (includeCompatibilityNotice) {
    lines.push(
      "> Compatibility note: `SKILL.md` is the canonical, Agent Skills-compatible entrypoint for this skill.",
      "",
    );
  }

  lines.push(
    generatedNotice(specRelativePath).trimEnd(),
    "",
    "## Overview",
    "",
    skill.description,
    "",
    "## When to Use This Skill",
    "",
    formatBulletList(skill.appliesWhen),
    "",
  );

  // Reference table (progressive disclosure)
  if (skill.references?.length && referenceBasePath) {
    lines.push(renderReferenceTable(skill, referenceBasePath));
  }

  // Scripts table (progressive disclosure)
  if (skill.scripts?.length && scriptsBasePath) {
    lines.push(renderScriptsTable(skill, scriptsBasePath));
  }

  // Assets table (progressive disclosure)
  if (skill.assets?.length && assetsBasePath) {
    lines.push(renderAssetsTable(skill, assetsBasePath));
  }

  lines.push(
    "## Rule Categories by Priority",
    "",
    renderCategoryTable(skill.categories),
    "",
    "## Quick Reference",
    "",
    renderQuickReference(skill),
    "",
    renderDetailedGuidance(skill),
    "",
  );

  return `${lines.join("\n").trimEnd()}\n`;
}

function renderSkillFrontmatter(skill, specRelativePath) {
  const metadata = {
    title: skill.title,
    version: skill.version,
    author: skill.author,
    generated_by: `Agent Jump Start v${TOOL_VERSION}`,
    generated_from: specRelativePath,
    ...skill.metadata,
  };

  const frontmatter = {
    name: skill.slug,
    description: buildSkillDiscoveryDescription(skill),
    license: skill.license ?? "MPL-2.0",
  };

  // Trigger and activation metadata — top-level frontmatter for ecosystem compatibility
  if (skill.triggers?.length) frontmatter.triggers = skill.triggers;
  if (skill.globs?.length) frontmatter.globs = skill.globs;
  if (skill.alwaysApply !== undefined) frontmatter.alwaysApply = skill.alwaysApply;
  if (skill.manualOnly !== undefined) frontmatter.manualOnly = skill.manualOnly;
  if (skill.relatedSkills?.length) frontmatter.relatedSkills = skill.relatedSkills;
  if (skill.compatibility?.length) frontmatter.compatibility = skill.compatibility;

  if (skill.dependencies?.length) frontmatter.dependencies = skill.dependencies;
  frontmatter.metadata = metadata;

  return `---\n${renderYamlObjectLines(frontmatter).join("\n")}\n---\n`;
}

function renderSkillFile(skill, specRelativePath, referenceBasePath = "references") {
  return `${renderSkillFrontmatter(skill, specRelativePath)}\n${renderSkillBody(skill, specRelativePath, { referenceBasePath, scriptsBasePath: "scripts", assetsBasePath: "assets" })}`;
}

function renderLegacySkillGuide(skill, specRelativePath) {
  return renderSkillBody(skill, specRelativePath, { includeCompatibilityNotice: true });
}

function buildCanonicalSkillPackage(skill, specRelativePath) {
  const canonicalPackageFiles = {
    "SKILL.md": renderSkillFile(skill, specRelativePath),
    "AGENTS.md": renderLegacySkillGuide(skill, specRelativePath),
  };

  if (skill.references?.length) {
    for (const ref of skill.references) {
      canonicalPackageFiles[`references/${ref.name}`] = ref.content;
    }
  }

  if (skill.scripts?.length) {
    for (const script of skill.scripts) {
      canonicalPackageFiles[`scripts/${script.name}`] = script.content;
    }
  }

  if (skill.assets?.length) {
    for (const asset of skill.assets) {
      canonicalPackageFiles[`assets/${asset.name}`] = asset.content;
    }
  }

  return canonicalPackageFiles;
}

function mountSkillPackage(generatedFiles, baseDir, packageFiles, { includeLegacyGuide = false } = {}) {
  for (const [relativePath, content] of Object.entries(packageFiles)) {
    if (!includeLegacyGuide && relativePath === "AGENTS.md") {
      continue;
    }
    generatedFiles[`${baseDir}/${relativePath}`] = content;
  }
}

