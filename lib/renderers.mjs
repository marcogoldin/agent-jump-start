function canonicalSourceNotice(canonicalPath) {
  return `> This file is a compatibility mirror of [\`${canonicalPath}\`](${canonicalPath}).\n> Edit the canonical spec and rerun \`render\` — do not hand-edit this file or its canonical source independently.\n`;
}

function renderWorkspaceInstructions(spec, specRelativePath, { includeSkillSummaries = false, mirrorOf = null } = {}) {
  const lines = [
    "# Workspace Instructions",
    "",
  ];

  if (mirrorOf) {
    lines.push(canonicalSourceNotice(mirrorOf).trimEnd(), "");
  }

  lines.push(generatedNotice(specRelativePath).trimEnd(), "");

  const workspace = spec.workspaceInstructions;
  if (workspace.packageManagerRule) {
    lines.push(workspace.packageManagerRule, "");
  }
  if (workspace.runtimeRule) {
    lines.push(workspace.runtimeRule, "");
  }
  if (spec.project?.components?.length) {
    lines.push("This workspace contains:");
    for (const component of spec.project.components) {
      lines.push(`- ${component}`);
    }
    lines.push("");
  }

  for (const section of workspace.sections ?? []) {
    lines.push(`${section.title}:`);
    lines.push(formatBulletList(section.rules));
    lines.push("");
  }

  if (workspace.validation?.length) {
    lines.push("Validation rules:");
    lines.push(formatBulletList(workspace.validation));
    lines.push("");
  }

  if (includeSkillSummaries && spec.skills?.length) {
    lines.push("## Skills", "");
    for (const skill of spec.skills) {
      lines.push(renderSkillSummarySection(skill), "");
    }
  }

  return `${lines.join("\n").trimEnd()}\n`;
}

