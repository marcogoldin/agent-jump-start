export function discoverPackageRoot() {
  const scriptDir = dirname(new URL(import.meta.url).pathname);
  return resolve(scriptDir, "..");
}

export function listAvailableProfiles() {
  const packageRoot = discoverPackageRoot();
  const profileDir = join(packageRoot, "specs", "profiles");
  if (!existsSync(profileDir)) {
    return [];
  }
  return readdirSync(profileDir)
    .filter((f) => f.endsWith(".profile.yaml"))
    .map((f) => ({
      file: f,
      path: join(profileDir, f),
      name: f.replace(".profile.yaml", ""),
    }));
}

// ---------------------------------------------------------------------------
// Disabled-agent root cleanup
//
// After stale managed files are removed, disabled-agent cleanup targets may
// remain on disk. Targets come in two forms:
//   - Directories (path ends with "/"): removed if empty, reported if
//     non-empty with unmanaged content.
//   - Files (no trailing "/"): removed if AJS-managed (provenance marker),
//     reported if unmanaged (operator-authored).
// ---------------------------------------------------------------------------

/**
 * Clean disabled-agent targets and report residuals.
 *
 * @param {Set<string>|null} enabledAgents — the set of enabled agent IDs, or null for all
 * @param {string} targetRoot — absolute path to the workspace root
 * @param {Map} agentRegistry — the agent registry Map
 * @returns {{ removed: string[], residuals: { agentId: string, displayName: string, root: string }[] }}
 */
export function cleanDisabledAgentRoots(enabledAgents, targetRoot, agentRegistry) {
  const removed = [];
  const residuals = [];

  if (!enabledAgents) {
    return { removed, residuals };
  }

  for (const [agentId, entry] of agentRegistry) {
    if (enabledAgents.has(agentId)) continue;
    for (const target of entry.agentRoots ?? []) {
      const targetPath = join(resolve(targetRoot), target);
      if (!existsSync(targetPath)) continue;

      const isDirectoryTarget = target.endsWith("/");

      if (isDirectoryTarget) {
        try {
          if (!statSync(targetPath).isDirectory()) continue;
        } catch {
          continue;
        }

        const hasContent = directoryHasFiles(targetPath);
        if (!hasContent) {
          rmSync(targetPath, { recursive: true, force: true });
          removed.push(target);
        } else {
          residuals.push({ agentId, displayName: entry.displayName, root: target });
        }
      } else {
        // File target (e.g. GEMINI.md, CONVENTIONS.md, .windsurfrules, .roorules, .clinerules)
        try {
          if (!statSync(targetPath).isFile()) continue;
        } catch {
          continue;
        }

        const kind = classifyExistingFile(targetPath);
        if (kind === "managed") {
          rmSync(targetPath, { force: true });
          removed.push(target);
        } else {
          // Unmanaged operator-authored file — do not delete, report
          residuals.push({ agentId, displayName: entry.displayName, root: target });
        }
      }
    }
  }

  return { removed, residuals };
}

function directoryHasFiles(dirPath) {
  try {
    const entries = readdirSync(dirPath);
    for (const entry of entries) {
      const entryPath = join(dirPath, entry);
      try {
        const stats = statSync(entryPath);
        if (stats.isFile()) return true;
        if (stats.isDirectory() && directoryHasFiles(entryPath)) return true;
      } catch {
        continue;
      }
    }
  } catch {
    return false;
  }
  return false;
}

export function listManagedFiles(rootPath) {
  const entries = [];

  function walk(currentPath) {
    for (const entry of readdirSync(currentPath)) {
      const absolutePath = join(currentPath, entry);
      const relativePath = relative(rootPath, absolutePath);
      const stats = statSync(absolutePath);
      if (stats.isDirectory()) {
        walk(absolutePath);
        continue;
      }
      entries.push(relativePath);
    }
  }

  walk(rootPath);
  return entries.sort();
}
